package model;

public class Calculadora {
	
	public double somar(double a, double b) {
	    return a + b;
	}
	
	public double subtrair(double a, double b) {
		return a - b;
	}
	
	public double divisão(double a, double b) {
		return a / b;
	}
	
	public double multiplicação(double a, double b) {
		return a * b;
	}
	
}


